    public void getData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (int i = 0; i < this.ecosystem.getWorkQueue().getWorkRequestList().size(); i++) {
            WorkRequest ongoing = this.ecosystem.getWorkQueue().getWorkRequestList().get(i);
            if (ongoing instanceof StockBuyWorkQueue) {
                StockBuyWorkQueue temp = (StockBuyWorkQueue) ongoing;
                if (temp.getStatusType() == StockBuyWorkQueue.StatusType.Completed) {
                    allRequests.add(temp);
                    Object[] row = {
                        temp.getOraganization(),
                        temp.getCompanyName(),
                        temp.getQuantity(),
                        temp.getPrice(),
                        temp.getModifiedAt(),
                        temp.getStatusType()
                    };
                    model.addRow(row);
                }
            }
        }
    }

