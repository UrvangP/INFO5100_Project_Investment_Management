    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked

        selectedRequest.setStatusType(StockBuyWorkQueue.StatusType.Awaiting);

        StockSellWorkRequest newRequest = new StockSellWorkRequest(
            this.account,
            selectedRequest.getRaisedTo(),
            new Date(),
            null,
            StockSellWorkRequest.StatusType.Initiated,
            selectedRequest.getPrice(),
            selectedRequest.getQuantity(),
            new Date(),
            selectedRequest.getCompanyName(),
            selectedRequest.getAssetName(),
            selectedRequest.getOraganization()
        );
        this.ecosystem.getWorkQueue().getWorkRequestList().add(newRequest);
        getData();
        JOptionPane.showMessageDialog(this, "Investment sold successfully!", "Investment", INFORMATION_MESSAGE);

    }//GEN-LAST:event_jLabel1MouseClicked


